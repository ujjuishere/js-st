// switch is better than multiple if else

const no = 3;

switch(no){
    case 1:
        console.log("Jan");
        break;
        
    case 2:
        console.log("Feb");
        break;
        
    case 3:
        console.log("March");
        break;
        
    case 4:
        console.log("April");
        break;
    default:
        break;
}